// FAQS toggle button

// Importing all the elements/tags

// 1st FAQ
let fAnswer1 = document.getElementById("faq-1");
let plusBtn1 = document.getElementById("plusBtn1");
let minusBtn1 = document.getElementById("minusBtn1");
let spanHide1 = document.getElementById("spanHide1");

// Adding event listener to the plus button

function funcPlusBtn1() {
    plusBtn1.style.display = "none";
    spanHide1.style.display = "block";
    fAnswer1.style.display = "block";
}

function funcMinusBtn1() {
    plusBtn1.style.display = "block";
    spanHide1.style.display = "none";
    fAnswer1.style.display = "none";
}

